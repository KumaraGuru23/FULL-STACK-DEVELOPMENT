package chapter2;

public class SquareAndCube {

	public static void main(String[] args) {
		
		int num = 5;
		
	    int square = num * num;
	    int cube = num * num* num;
	    
	    System.out.println("The Value Of A Number Is : "+num);
	    System.out.println("The Value Of A square Is : "+square);
	    System.out.println("The Value Of A cube Is : "+cube);
	    
		
	}

}
