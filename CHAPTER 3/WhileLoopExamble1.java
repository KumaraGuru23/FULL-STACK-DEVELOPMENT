package chapter3;

public class WhileLoopExamble1 {
	
	    public static void main(String[] args) {
	        int i = 2;  // starting number

	        // while loop
	        while (i <= 20) {
	            System.out.println(i);
	            i += 2;  // increment by 2 to get even numbers
	        }
	    }
	}
