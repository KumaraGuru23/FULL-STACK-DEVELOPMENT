package chapter3;

public class WhileLoopExamble3 {
	
	    public static void main(String[] args) {
	        int num = 5;  // Example number
	        int i = 1;

	        while (i <= 10) {
	            System.out.println(num + " x " + i + " = " + (num * i));
	            i++;
	        }
	    }
	}
